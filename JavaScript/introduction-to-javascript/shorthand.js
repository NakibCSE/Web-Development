// Add something to the previous value
var price1 = 31;
var price2 = 10;

// += means adding some value to the existing value
price1+=10;

console.log(price1);

//-= means deducting some values from the current value of the variable
price2-=5;

console.log(price2);

// *= means multiplying the current values with another value
price1*= 3;

// /= means dividing the current vale by another number 
price1/=3;

// ++ means increase by 1
price1 ++;

//-- means decreasing the value by one
price1 --;